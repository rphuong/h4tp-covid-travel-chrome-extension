function isState(input) {
  if (input.length == 2) {
    return StateAbbrevs.has(input.toUpperCase());
  } else {
    return States.has(input.toLowerCase().split(' ')
      .map(word => word.charAt(0).toUpperCase() + word.slice(1))
      .join(' '))
  }

}

